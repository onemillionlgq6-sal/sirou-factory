import { useState } from "react";
import { motion } from "framer-motion";
import { Eye, Smartphone, Monitor, Tablet, Code2 } from "lucide-react";
import {
  Tooltip,
  TooltipContent,
  TooltipTrigger,
} from "@/components/ui/tooltip";
import { useI18n } from "@/lib/i18n";

interface AppPreviewProps {
  isGenerated: boolean;
  appName: string;
}

type ViewMode = "desktop" | "tablet" | "mobile";

const viewWidths: Record<ViewMode, string> = {
  desktop: "w-full",
  tablet: "w-[768px] max-w-full",
  mobile: "w-[375px] max-w-full",
};

const AppPreview = ({ isGenerated, appName }: AppPreviewProps) => {
  const [viewMode, setViewMode] = useState<ViewMode>("desktop");
  const [showCode, setShowCode] = useState(false);
  const { t } = useI18n();

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ delay: 0.3 }}
      className="sf-glass rounded-2xl p-6"
    >
      <div className="flex items-center justify-between mb-4">
        <div className="flex items-center gap-2">
          <Eye className="h-5 w-5 text-accent" />
          <h2 className="text-lg font-semibold text-foreground">
            {t("preview.title")}
          </h2>
          <Tooltip>
            <TooltipTrigger>
              <span className="text-xs sf-glass-subtle text-muted-foreground px-2 py-0.5 rounded-full cursor-help">
                ?
              </span>
            </TooltipTrigger>
            <TooltipContent>
              <p className="max-w-xs text-sm">{t("preview.tooltip")}</p>
            </TooltipContent>
          </Tooltip>
        </div>

        <div className="flex items-center gap-1 sf-glass-subtle rounded-lg p-1">
          {([
            { mode: "desktop" as const, icon: Monitor },
            { mode: "tablet" as const, icon: Tablet },
            { mode: "mobile" as const, icon: Smartphone },
          ]).map(({ mode, icon: Icon }) => (
            <button
              key={mode}
              onClick={() => setViewMode(mode)}
              className={`p-2 rounded-md transition-all ${
                viewMode === mode
                  ? "sf-glass-strong text-foreground shadow-sm"
                  : "text-muted-foreground hover:text-foreground"
              }`}
            >
              <Icon className="h-4 w-4" />
            </button>
          ))}
          <div className="w-px h-5 bg-foreground/20 mx-1" />
          <button
            onClick={() => setShowCode(!showCode)}
            className={`p-2 rounded-md transition-all ${
              showCode
                ? "sf-glass-strong text-foreground shadow-sm"
                : "text-muted-foreground hover:text-foreground"
            }`}
          >
            <Code2 className="h-4 w-4" />
          </button>
        </div>
      </div>

      <div
        className={`mx-auto rounded-xl overflow-hidden sf-glass-subtle transition-all duration-300 ${viewWidths[viewMode]}`}
      >
        {!isGenerated ? (
          <div className="flex flex-col items-center justify-center h-[400px] text-muted-foreground">
            <Monitor className="h-16 w-16 mb-4 opacity-20" />
            <p className="text-sm">{t("preview.empty")}</p>
            <p className="text-xs mt-1 opacity-60">{t("preview.hint")}</p>
          </div>
        ) : showCode ? (
          <div className="h-[400px] overflow-auto p-4 bg-black/40 text-foreground font-mono text-xs leading-relaxed">
            <pre>{`// ${appName} — Generated by Sirou Factory
// Clean, organized code with full comments

import React from 'react';

/**
 * Main application component
 * Auto-generated with security best practices
 */
const App: React.FC = () => {
  return (
    <div className="min-h-screen bg-background">
      <header className="border-b border-border p-4">
        <h1 className="text-xl font-bold">${appName}</h1>
      </header>
      <main className="container mx-auto p-6">
        {/* Your app content here */}
      </main>
    </div>
  );
};

export default App;`}</pre>
          </div>
        ) : (
          <div className="h-[400px] flex flex-col">
            <div className="flex items-center gap-2 px-4 py-2 border-b border-foreground/10 sf-glass-subtle">
              <div className="flex gap-1.5">
                <div className="w-3 h-3 rounded-full bg-sf-danger/60" />
                <div className="w-3 h-3 rounded-full bg-sf-caution/60" />
                <div className="w-3 h-3 rounded-full bg-sf-safe/60" />
              </div>
              <span className="text-xs text-muted-foreground ms-2">
                {appName}
              </span>
            </div>
            <div className="flex-1 flex flex-col items-center justify-center p-8">
              <div className="w-full max-w-sm space-y-4">
                <div className="h-8 sf-gradient-bg rounded-lg opacity-80" />
                <div className="h-4 sf-glass-subtle rounded w-3/4" />
                <div className="h-4 sf-glass-subtle rounded w-1/2" />
                <div className="grid grid-cols-2 gap-3 mt-6">
                  <div className="h-24 sf-glass-subtle rounded-xl" />
                  <div className="h-24 sf-glass-subtle rounded-xl" />
                </div>
                <div className="h-10 sf-gradient-bg rounded-lg opacity-60 mt-4" />
              </div>
            </div>
          </div>
        )}
      </div>
    </motion.div>
  );
};

export default AppPreview;
